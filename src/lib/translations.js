export const useTranslation = (language) => {
  const t = (path) => {
    const keys = path.split('.');
    let value = translations[language];
    for (const key of keys) {
      if (value && value[key]) {
        value = value[key];
      } else {
        return path; // Fallback to path if translation not found
      }
    }
    return value;
  };

  return { t };
};
