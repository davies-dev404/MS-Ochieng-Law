export const getBlogPosts = () => {
  if (typeof window !== 'undefined') {
    const localBlogs = localStorage.getItem('mso_blogs');
    if (localBlogs) {
      try {
        const parsed = JSON.parse(localBlogs);
        // Combine local blogs with default blogs (local blogs first)
        return [...parsed, ...defaultBlogPosts];
      } catch (e) {
        console.error("Failed to parse local blogs", e);
      }
    }
  }
  return defaultBlogPosts;
};

export const saveBlogPost = (post) => {
  if (typeof window !== 'undefined') {
    const localBlogs = localStorage.getItem('mso_blogs');
    let blogs = [];
    if (localBlogs) {
      try {
        blogs = JSON.parse(localBlogs);
      } catch (e) {}
    }
    blogs.unshift(post);
    localStorage.setItem('mso_blogs', JSON.stringify(blogs));
  }
};
